/** 
 * Aritméticos
 * +  Adição / Concatenação
 * ++ Incremento
 * -  Subtração
 * -- Decremento
 * *  Multiplicação
 * /  Divisão
 * ** Potenciação
 * %  Resto de divisão / Módulo
 * 
*/

const num1 = 10;
const num2 = 3;

console.log( num1 + num2 );
console.log( num1 - num2 );
console.log( num1 * num2 );
console.log( num1 / num2 );
console.log( num1 ** num2 );
console.log( num1 % num2 );